// Obtiene referencias a los elementos relevantes
const navLogo = document.getElementById('nav-logo');
const seccionNaranja = document.querySelector('.section-naranja');
const seccionBlanca = document.querySelector('.section-blanca');
const seccionTercera = document.querySelector('.section-tercera');
const seccionCuarta = document.querySelector('.section-cuarta');
const seccionQuinta = document.querySelector('.section-quinta');
const btnLogo = document.getElementById('btnLogo');
const navLinks = document.querySelectorAll('.navbar-nav .nav-link');

// Función para cambiar el logo en función de la posición de la página
function cambiarLogoEnScroll() {
  const scrollY = window.scrollY;
  const naranjaY = seccionNaranja.offsetTop;
  const blancaY = seccionBlanca.offsetTop;
  const terceraY = seccionTercera.offsetTop;
  const cuartaY = seccionCuarta.offsetTop;
  const quintaY = seccionQuinta.offsetTop

  if (scrollY >= naranjaY && scrollY < blancaY) {
    navLogo.src = '/assets/logo-blanco.png';
    btnLogo.src = '/assets/group.png';
  } else if (scrollY >= blancaY && scrollY < terceraY) {
    navLogo.src = '/assets/component1.png';
    btnLogo.src = '/assets/boton-menu-naranja.png';
  } else if(scrollY >= terceraY && scrollY < cuartaY) {
    navLogo.src = '/assets/logo-blanco.png'; // Cambiar a blanco en la tercera sección
    btnLogo.src = '/assets/group.png';
  } else if(scrollY >= cuartaY && scrollY < quintaY){
    navLogo.src = '/assets/component1.png';
    btnLogo.src = '/assets/boton-menu-naranja.png';
  }else{
    navLogo.src = '/assets/logo-blanco.png';
    btnLogo.src = '/assets/group.png';
  }

   // Cambia el color del texto en función de la posición
   if (scrollY >= naranjaY && scrollY < blancaY) {
    navLinks.forEach((link) => link.style.color = '#fff');
  } else if (scrollY >= blancaY && scrollY < terceraY) {
    navLinks.forEach((link) => link.style.color = '#FF9900');
  } else if(scrollY >= terceraY && scrollY < cuartaY){
    navLinks.forEach((link) => link.style.color = '#FFF');
  } else if(scrollY >= cuartaY && scrollY < quintaY){
    navLinks.forEach((link) => link.style.color = '#FF9900');
  }else{
    navLinks.forEach((link) => link.style.color = '#FFF');
  }
}


// Agrega un evento scroll para detectar cambios en la posición de la página
window.addEventListener('scroll', cambiarLogoEnScroll);

// Llama a la función inicialmente para configurar el logo
cambiarLogoEnScroll();

